package three;

public class TestSwitch {
    public static void main(String[] args) {
        int choice = 1;
        switch (choice) {
            case 1:
                System.out.println("nikal");
                break;
            case 2:
                System.out.println("yhn");
                break;
            case 3:
                System.out.println("se");
            case 4:
                System.out.println("nikal yhn se");
            default:
                System.out.println("kuch nhi input diyo ho bhai mere");
        }
    }
}
